package theory.tasksClassFive;

public class Friend {
    public String name;
    public int age;
    public String gender;
    public Friend(String name, int age, String gender) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }
    public Friend(String name, int age) {
        this.name = name;
        this.age = age;
    }
    public Friend(String name) {
        this.name = name;
    }
}
